export const seedTemplates = internalMutation({
  args: {},
  handler: async (ctx) => {
    const admin = await ctx.db
      .query("users")
      .withIndex("by_role", (q) => q.eq("role", "admin"))
      .take(1);

    if (admin.length === 0) {
      throw new Error("No admin user found. Create an admin user first.");
    }

    const adminId = admin[0]._id;
    let created = 0;
    let skipped = 0;

    for (const instrument of INSTRUMENTS) {
      // Skip if shortCode already exists
      const existing = await ctx.db
        .query("assessmentTemplates")
        .withIndex("by_shortCode", (q) => q.eq("shortCode", instrument.shortCode))
        .take(1);

      if (existing.length > 0) {
        skipped++;
        continue;
      }

      // eslint-disable-next-line @typescript-eslint/no-explicit-any
      const data: any = {
        ...instrument,
        createdBy: adminId,
        createdAt: Date.now(),
        updatedAt: Date.now(),
      };
      await ctx.db.insert("assessmentTemplates", data);
      created++;
    }

    return { created, skipped, total: INSTRUMENTS.length };
  },
});
